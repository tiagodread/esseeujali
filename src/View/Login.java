    private void btAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAcessarActionPerformed
        if (txtLogin.getText().equals("") || txtSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe o login e a senha!");
            txtLogin.setText("");
            txtLogin.requestFocus();
            txtSenha.setText("");
        } else {

            ResultSet res = udao.logar(txtLogin.getText(), txtSenha.getText());
            try {
                if (!res.isBeforeFirst()) {
                    JOptionPane.showMessageDialog(null, "Login ou senha inv치lidos!");
                    txtLogin.setText("");
                    txtLogin.requestFocus();
                    txtSenha.setText("");
                }
                if (res.next()) {
                    Principal principal = new Principal(res.getInt("idusuario"));
                    principal.setVisible(true);
                    dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btAcessarActionPerformed

