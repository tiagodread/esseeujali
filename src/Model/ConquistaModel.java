package Model;

/**
 *
 * @author Tiago
 */
public class ConquistaModel {

    private int id;
    private String nome;
    private String imagem;
    private String genero;

    public ConquistaModel(int id, String nome, String imagem, String genero) {
        this.id = id;
        this.nome = nome;
        this.imagem = imagem;
        this.genero = genero;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getImagem() {
        return imagem;
    }

    public void setImagem(String imagem) {
        this.imagem = imagem;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
}
